package com.cookandroid.mini_notice;

public class UserInfo {
    String id, password, name, email, reg_date;

    public UserInfo(String id, String password, String name, String email, String reg_date) {
        this.id = id;
        this.password = password;
        this.name = name;
        this.email = email;
        this.reg_date = reg_date;
    }
}
